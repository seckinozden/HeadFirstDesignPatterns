public class PopcornPopper {
    public void on() {
        System.out.println("Popcorn Popper is turned on.");
    }

    public void off() {
        System.out.println("Popcorn Popper is turned off");
    }

    public void pop() {
        System.out.println("Popcorn Popper is popping popcorns.");
    }
}
