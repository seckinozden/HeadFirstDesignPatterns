package com.ch6.command;

public class GarageDoor {

    public void up() {
        System.out.println("Garage door is up.");
    }

    public void down() {
        System.out.println("Garage door is down.");
    }

    public void stop() {
        System.out.println("Garage door stopped.");
    }

    public void lightOn() {
        System.out.println("Garage door lights turned on.");
    }

    public void lightOff() {
        System.out.println("Garage door lights turned off.");
    }
}
